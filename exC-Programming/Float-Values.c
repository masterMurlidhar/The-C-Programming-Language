
// Arithmetic Operations using float value

#include <stdio.h>


int main(int argc, char const *argv[])

{

    printf("Arithmetic Operations using float value\n");

    int a=13;

    float b=6.5;

    printf("a+b=%f\n", a+b);

    printf("a-b=%f\n", a-b);

    printf("a*b=%f\n", a*b);

    printf("a/b=%f\n", a/b);

    return 0;

}


